    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (txtcodigo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Selecciona un cliente de la tabla para editar");
            return;
        }
        
        if (!validarFormulario()) {
            return;
        }
        
        try {
            int id = Integer.parseInt(txtcodigo.getText());
            String nombre = txtnombre.getText().trim();
            String telefono = txttelefono.getText().trim();
            String direccion = txtdireccion.getText().trim();
            
            if (clienteController.actualizarCliente(id, nombre, telefono, direccion)) {
                JOptionPane.showMessageDialog(this, "Cliente actualizado exitosamente");
                cargarDatos();
                btnLimpiarActionPerformed(evt);
            } else {
                JOptionPane.showMessageDialog(this, "Error al actualizar el cliente");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Código de cliente inválido");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (txtcodigo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Selecciona un cliente de la tabla para eliminar");
            return;
        }
        
        try {
            int id = Integer.parseInt(txtcodigo.getText());
            String nombre = txtnombre.getText().trim();
            
            int confirmacion = JOptionPane.showConfirmDialog(this,
                    "¿Estás seguro de eliminar al cliente: " + nombre + "?",
                    "Confirmar Eliminación",
                    JOptionPane.YES_NO_OPTION);
            
            if (confirmacion == JOptionPane.YES_OPTION) {
                if (clienteController.eliminarCliente(id)) {
                    JOptionPane.showMessageDialog(this, "Cliente eliminado exitosamente");
                    cargarDatos();
                    btnLimpiarActionPerformed(evt);
                } else {
                    JOptionPane.showMessageDialog(this, "Error al eliminar el cliente");
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Código de cliente inválido");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void txtbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtbuscarActionPerformed
        String textoBusqueda = txtbuscar.getText().trim();
        
        if (textoBusqueda.isEmpty()) {
            cargarDatos(); // Si no hay texto, cargar todos
            return;
        }

        // Búsqueda simple en todos los campos
        try {
            modeloTabla.setRowCount(0); // Limpiar tabla
            java.util.List<com.maffy.models.Cliente> clientes = clienteController.obtenerTodosClientes();
            
            for (com.maffy.models.Cliente cliente : clientes) {
                // Buscar en todos los campos
                if (String.valueOf(cliente.getId()).contains(textoBusqueda)
                        || cliente.getNombre().toLowerCase().contains(textoBusqueda.toLowerCase())
                        || cliente.getTelefono().contains(textoBusqueda)
                        || cliente.getDireccion().toLowerCase().contains(textoBusqueda.toLowerCase())) {
                    
                    Object[] fila = {
                        cliente.getId(),
                        cliente.getNombre(),
                        cliente.getTelefono(),
                        cliente.getDireccion()
                    };
                    modeloTabla.addRow(fila);
                }
            }
        } catch (Exception e) {
            System.err.println("Error en búsqueda: " + e.getMessage());
        }
    }//GEN-LAST:event_txtbuscarActionPerformed


