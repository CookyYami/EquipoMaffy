    private void configurarEventos() {
    // Evento cuando se selecciona una fila en la tabla
    tablaCliente.getSelectionModel().addListSelectionListener(e -> {
        if (!e.getValueIsAdjusting()) {
            cargarClienteSeleccionado();
        }
    });
    
    // También puedes agregar evento doble clic si quieres
    tablaCliente.addMouseListener(new java.awt.event.MouseAdapter() {
        @Override
        public void mouseClicked(java.awt.event.MouseEvent evt) {
            if (evt.getClickCount() == 1) { // Un clic
                cargarClienteSeleccionado();
            }
        }
    });
}
    
    private void configurarTabla() {
        modeloTabla = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Hacer la tabla no editable
            }
        };
        
        modeloTabla.addColumn("CODIGO");
        modeloTabla.addColumn("NOMBRE");
        modeloTabla.addColumn("TELEFONO");
        modeloTabla.addColumn("DIRECCION");
        
        tablaCliente.setModel(modeloTabla);
    }
    
    private void cargarDatos() {
        // Cargar todos los clientes en la tabla
        if (clienteController.cargarClientesEnTabla(modeloTabla)) {
            System.out.println("Datos cargados correctamente en PanelCliente");
        } else {
            JOptionPane.showMessageDialog(this,
                    "Error al cargar los clientes desde la base de datos",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void cargarClienteSeleccionado() {
        int filaSeleccionada = tablaCliente.getSelectedRow();
        if (filaSeleccionada >= 0) {
            // Cargar datos del cliente seleccionado en los campos
            txtcodigo.setText(modeloTabla.getValueAt(filaSeleccionada, 0).toString());
            txtnombre.setText(modeloTabla.getValueAt(filaSeleccionada, 1).toString());
            txttelefono.setText(modeloTabla.getValueAt(filaSeleccionada, 2).toString());
            txtdireccion.setText(modeloTabla.getValueAt(filaSeleccionada, 3).toString());
        }
    }
    
    private boolean validarFormulario() {
        if (txtnombre.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El nombre es obligatorio");
            txtnombre.requestFocus();
            return false;
        }
        
        if (txtdireccion.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El email es obligatorio");
            txtdireccion.requestFocus();
            return false;
        }

        
        return true;
    }

